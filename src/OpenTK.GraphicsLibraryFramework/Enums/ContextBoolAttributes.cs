namespace OpenToolkit.GraphicsLibraryFramework
{
    /// <summary>
    /// Context related boolean attributes.
    /// </summary>
    /// <seealso cref="IGLFW.WindowHint(ContextBoolAttributes,bool)"/>
    public enum ContextBoolAttributes
    {
        /// <summary>
        /// Is <c>true</c> if the window's context is an OpenGL forward-compatible one, or <c>false</c> otherwise.
        /// </summary>
        OpenGLForwardCompat = 0x00022006,

        /// <summary>
        /// Is <c>true</c> if the window's context is an OpenGL debug context, or <c>false</c> otherwise.
        /// </summary>
        OpenGLDebugContext = 0x00022007,

        /// <summary>
        /// Specifies whether errors should be generated by the context.
        /// If enabled, situations that would have generated errors instead cause undefined behavior.
        /// </summary>
        ContextNoError = 0x0002200A,
    }
}
